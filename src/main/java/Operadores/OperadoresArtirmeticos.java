package Operadores;

/**
  +: suma
  -: resta
  *: multiplicación
  /: división
  %: resto de la división
 */

public class OperadoresArtirmeticos {
    public static void main(String args[]){
        float a = 8, b = 3, c;

        c = a+b;
        System.out.println(c);

        c = a-b;
        System.out.println(c);

        c = a*b;
        System.out.println(c);

        c = a/b;
        System.out.println(c);
    }
}
